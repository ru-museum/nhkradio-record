   echo " ※ 番組表は、自由に追加変更可能です。"
   echo " ※ ID指定の場合、録音ファイルは「番組タイトル名」のディレクトリに保存されます。"
   echo " ※ 「CRON曜日」設定での定時録音の方法については README.md を参照して下さい。"
   echo "  "
  exit 3
}

# --------------------------------------------------
# 無効オプションERROR表示
function usage(){
  cat <<EOM

  USAGE: \$ bash $0 -i [number] -w [am|fm] -r [00:00:00] -t [title] -d [directory] -s [00(秒)|00m 00s]

  （A）録音タイトル名を指定：
      ※ -d オプション指定の無い場合は、当スクリプトの直下に保存されます。
      ※ -t オプション指定の無い場合は、録音ファイルタイトルは「NHKラジオ放送番組」となります。
     【例】AM放送を30秒後に15分間録音しタイトルを「NHKきょうのニュース」とし「audio」ディレクトリへ保存：
         \$ bash $0 -w am -r 00:15:00 -t NHKきょうのニュース -d audio -s 30

  （B）番組表の ID により番組を指定：
      ※ ID による番組を個別に予約録音します。
      ※ 録音時間は「番組表」の「時間」データに準じます（指定する場合：-r [00:00:00]）。
      ※ 保存ディレクトリは -d で指定が出来ます（通常は「番組名」ディレクトリ）。
     【例】10分後に ID「2」の番組を録音開始し「番組名」ディレクトリへ保存：
         \$ bash $0 -i 2 -s 10m
         \$ bash $0 -i 2 -s 10m -d audio (「audio」ディレクトリへ保存) 

  （C）CRON による定時録音の方法については README.md を参照して下さい。"
EOM
}

