# 曜日設定 
getDateTime() {
  arr=("日" "月" "火" "水" "木" "金" "土")
  DAY=`date '+%w'`
  _DAY="(${arr[$DAY]})"
  YEAR=`date '+%Y'`
  DATE1=`date '+%m%d'`
  DATE2=`date '+%H:%M'`
  echo "${YEAR}${DATE1}${_DAY}${DATE2}"
}

# 初級編・応用編のある語学
# 月火水木金土日 = 1234567
setEdition(){
  local ed=""
  # local d=`date '+%w'`
  local d=3 # ●●● TEST
  # 月火水: 123 => 初級編
  if test $d -gt 0 -a $d -lt 4 ; then
     ed="初級編"
  # 木金: 45 => 応用編
  elif test $d -eq 4 -o $d -eq 5 ; then
     ed="応用編"
  fi
  echo $ed
}

